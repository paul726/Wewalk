package com.nus.wewalk.ui.challenges;

public class RankBean {

    private String steps;
    private Object distance;
    private Object calories;
    private Object targetSteps;
    private Object progress;
    private String userName;
    private String avatar;

    public String getSteps() {
        return steps;
    }

    public void setSteps(String steps) {
        this.steps = steps;
    }

    public Object getDistance() {
        return distance;
    }

    public void setDistance(Object distance) {
        this.distance = distance;
    }

    public Object getCalories() {
        return calories;
    }

    public void setCalories(Object calories) {
        this.calories = calories;
    }

    public Object getTargetSteps() {
        return targetSteps;
    }

    public void setTargetSteps(Object targetSteps) {
        this.targetSteps = targetSteps;
    }

    public Object getProgress() {
        return progress;
    }

    public void setProgress(Object progress) {
        this.progress = progress;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
}
